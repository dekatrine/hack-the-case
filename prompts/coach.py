def build_coach_context(step_id: str, step_title: str, frameworks: list, 
                         case_text: str, answer_text: str, chat_history: list,
                         all_answers: dict, all_steps: list) -> str:
    """
    Строит контекст для коуча с учётом ВСЕХ предыдущих этапов.
    
    Это ключевое улучшение: коуч теперь видит, что студент делал раньше,
    и может указывать на противоречия / ссылаться на предыдущие ответы.
    """
    from utils.llm import truncate_smart
    
    # Саммари предыдущих этапов
    prev_summary = ""
    current_found = False
    for s in all_steps:
        if s["id"] == step_id:
            current_found = True
            break
        prev_answer = all_answers.get(s["id"], "")
        if prev_answer.strip():
            # Берём первые 200 символов каждого ответа
            short = truncate_smart(prev_answer, 200)
            prev_summary += f"\n- {s['title']}: {short}"
    
    # Формируем контекст
    context_parts = []
    
    # Кейс (умная обрезка)
    context_parts.append(f"КЕЙС:\n{truncate_smart(case_text, 1500)}")
    
    # Предыдущие этапы (НОВОЕ!)
    if prev_summary:
        context_parts.append(
            f"ОТВЕТЫ СТУДЕНТА НА ПРЕДЫДУЩИХ ЭТАПАХ:{prev_summary}\n"
            f"(Используй эту информацию, чтобы проверить связность решения)"
        )
    
    # Текущий этап
    context_parts.append(
        f"ТЕКУЩИЙ ЭТАП: {step_title}\n"
        f"Рекомендуемые фреймворки: {', '.join(frameworks)}"
    )
    
    # Ответ студента
    if answer_text.strip():
        context_parts.append(f"ОТВЕТ СТУДЕНТА НА ТЕКУЩИЙ БЛОК:\n{answer_text}")
    else:
        context_parts.append("ОТВЕТ СТУДЕНТА: (ещё не написал)")
    
    # История чата (последние 4 сообщения)
    if chat_history:
        recent = chat_history[-4:]
        history_text = "\n".join([
            f"{'Студент' if m['role']=='student' else 'Коуч'}: {m['text'][:150]}" 
            for m in recent
        ])
        context_parts.append(f"ИСТОРИЯ ДИАЛОГА:\n{history_text}")
    
    return "\n\n".join(context_parts)


def get_coach_system_prompt(step_id: str) -> str:
    """Собирает полный системный промпт коуча для конкретного этапа."""
    step_specific = STEP_COACH_PROMPTS.get(step_id, "")
    return f"{COACH_BASE}\n\n{step_specific}"


