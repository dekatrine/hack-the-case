def parse_scores_from_text(evaluation_text: str) -> list:
    """
    Извлечь числовые оценки из текста жюри для radar-chart.
    
    Ищет паттерны вида "N/10" или ": N/10" в тексте.
    Возвращает список из 10 чисел (или None если не удалось распарсить).
    """
    # Паттерн: число от 1 до 10, за которым /10
    pattern = r'(\d{1,2})\s*/\s*10'
    matches = re.findall(pattern, evaluation_text)
    
    scores = []
    for m in matches:
        score = int(m)
        if 1 <= score <= 10:
            scores.append(score)
    
    # Ожидаем ровно 10 оценок (по одной на критерий)
    if len(scores) >= 10:
        return scores[:10]
    
    return None  # Не удалось распарсить


def get_total_score(scores: list) -> int:
    """Посчитать общий балл из списка оценок."""
    if scores:
        return sum(scores)
    return 0


